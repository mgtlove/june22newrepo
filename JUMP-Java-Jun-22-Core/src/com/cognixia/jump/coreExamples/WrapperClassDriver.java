package com.cognixia.jump.coreExamples;

public class WrapperClassDriver {

	public static void main(String[] args) {
		
		// A look at wrapper classes in Java
		
		// the primitives
		byte b = 1;
		short s = 1;
		int i = 1 ;
		long l = 1;
		
		
		float f =1.0f;
		double d = 1.0d;
		
		int x = (int) f;
		
		char c = 'c';
		boolean bool = true;
		
		// their wrappers, so we can have objects
		Byte B = b;
		Short S = s;
		Integer I = i;
		Long L = l;
		Float F = f;
		Double D = d;
		
		Character C = c;
		Boolean Bool = bool;
		

	}

}
